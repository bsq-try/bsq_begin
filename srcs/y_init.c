void	init_info(t_map_info *info)
{	
	int idx;
	
	idx  = 0;
	info->max_row = 0;
	info->max_col = 0;
	info->row = 0;
	info->col = 0;
	info->val = 0;
	while (idx < 3)
		info->word[idx] = 0;
}
